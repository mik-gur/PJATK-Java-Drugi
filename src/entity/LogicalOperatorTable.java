package entity;

public class LogicalOperatorTable {
    boolean p;
    boolean q;

    public LogicalOperatorTable(boolean p, boolean q) {
        this.p = p;
        this.q = q;
    }

    public boolean isP() {
        return p;
    }

    public void setP(boolean p) {
        p = p;
    }

    public boolean isQ() {
        return q;
    }

    public void setQ(boolean q) {
        q = q;
    }
}
